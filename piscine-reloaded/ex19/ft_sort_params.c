#include <unistd.h>

void	ft_putchar(char c);

int	ft_strcmp(char *arg1, char *arg2)
{
	int	i;

	i = 0;
	while (arg1[i] && arg2[i] && arg1[i] == arg2[i])
		i++;
	return (arg1[i] - arg2[i]);
}

void	ft_sort(char **argv, int argc)
{
	int		i;
	int		j;
	char	*temp;

	i = 1;
	while (i < argc)
	{
		j = 1;
		while (j < argc - i)
		{
			if (ft_strcmp(argv[j], argv[j + 1]) > 0)
			{
				temp = argv[j +1];
				argv[j + 1] = argv[j];
				argv[j] = temp;
			}
			j++;
		}
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	i = 1;
	ft_sort(argv, argc);
	while (i < argc)
	{
		j = 0;
		while (argv[i][j])
		{
			ft_putchar(argv[i][j]);
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
